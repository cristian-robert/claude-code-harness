'use strict';

// Emits the Codex payload from PHE's canonical .claude/ tree.
//
// Canonical content lives ONCE, under .claude/. Codex reads different paths, so
// we DERIVE its tree:
//   .claude/skills/<n>/  -> .agents/skills/<n>/     (Codex's skill root)
//   .claude/agents/<n>.md -> .codex/agents/<n>.toml (Codex agents are TOML)
//                          -> .codex/config.toml
// Everything under .agents/ and .codex/ is GENERATED and overwritten on every
// init/update. Edit .claude/, never the generated trees.
//
// No hooks here — Phase 1 is guidance-only on Codex. Hooks land in Phase 2.

const fs = require('fs');
const path = require('path');
const { resolveModel, readModels, supportedEfforts, DEFAULT_MODELS } = require('./model-tiers');

const GENERATED_BY = 'Generated by perfect-harness-engineering — do not hand-edit.';

// Split "---\nkey: value\n---\nbody" into { fm: {...}, body: "..." }.
// Deliberately a flat scalar parser: agent frontmatter is flat key/value only.
function parseFrontmatter(text) {
  var t = String(text).replace(/\r\n/g, '\n');
  if (t.indexOf('---\n') !== 0) return { fm: {}, body: t };
  var end = t.indexOf('\n---', 3);
  if (end < 0) return { fm: {}, body: t };

  var fmBlock = t.slice(4, end);
  var afterMarker = t.indexOf('\n', end + 1);
  var body = afterMarker < 0 ? '' : t.slice(afterMarker + 1);

  var fm = {};
  var lines = fmBlock.split('\n');
  for (var i = 0; i < lines.length; i++) {
    var m = lines[i].match(/^([A-Za-z0-9_-]+)\s*:\s*(.*)$/);
    if (!m) continue;
    var v = m[2].trim();
    // Strip YAML quoting, then unescape the \" YAML uses inside a "..." scalar.
    if (v.length >= 2 &&
        ((v.charAt(0) === '"' && v.charAt(v.length - 1) === '"') ||
         (v.charAt(0) === "'" && v.charAt(v.length - 1) === "'"))) {
      v = v.slice(1, -1).replace(/\\"/g, '"');
    }
    fm[m[1]] = v;
  }
  return { fm: fm, body: body };
}

// TOML basic string (single line): escape backslash then quote.
function tomlBasic(s) {
  return '"' + String(s).replace(/\\/g, '\\\\').replace(/"/g, '\\"') + '"';
}

// TOML multi-line string. Prefer a LITERAL string ('''...''') — it needs no
// escaping at all, so Markdown bodies survive byte-for-byte. Fall back to a
// basic string (""") only when the body itself contains ''', escaping as we go.
function tomlMultiline(body) {
  var b = String(body).replace(/\s+$/, '');
  if (b.indexOf("'''") === -1) {
    return "'''\n" + b + "\n'''";
  }
  var escaped = b.replace(/\\/g, '\\\\').replace(/"""/g, '\\"\\"\\"');
  return '"""\n' + escaped + '\n"""';
}

// A Claude subagent (.claude/agents/<n>.md) -> a Codex subagent (.codex/agents/<n>.toml).
// Codex requires name + description + developer_instructions. It has no `tools` key.
//
// `onWarn` receives every non-fatal problem found while rendering. It defaults to stderr so a
// warning is never lost just because a caller ignored the return value.
function agentMdToToml(mdText, fallbackName, models, onWarn) {
  var warn = onWarn || function (msg) { console.warn('WARNING: ' + msg); };
  var parsed = parseFrontmatter(mdText);
  var name = parsed.fm.name || fallbackName;
  var description = parsed.fm.description || '';
  var body = parsed.body;

  if (!name) throw new Error('agentMdToToml: no name in frontmatter and no fallback name given');

  var lines = [
    '# ' + GENERATED_BY,
    '# Source: .claude/agents/' + name + '.md',
    '',
    'name = ' + tomlBasic(name),
    'description = ' + tomlBasic(description),
  ];

  // tier: is the canonical, harness-neutral pin. No tier -> no model key: Codex then
  // uses its own default, which beats us inventing one. An agent that DOES pin a tier
  // always gets a model, so a missing map can never silently drop it.
  var tier = parsed.fm.tier;
  if (tier) {
    var resolved = models || DEFAULT_MODELS;
    var model = resolveModel(resolved, 'codex', tier);
    lines.push('model = ' + tomlBasic(model));

    // An effort the model does not support fails at DISPATCH time, far from the file that
    // caused it — so it is validated here, against the ceilings recorded in the map beside
    // the IDs (harness.json -> models.efforts).
    //
    // The three-way split matters, and each branch was a bug once:
    //   known model + illegal level -> THROW. The original protection: an illegal level is a
    //     definite error, and the fix (lower the effort, raise the tier) is in the user's hands.
    //   known model + legal level   -> emit.
    //   NO recorded ceilings        -> WARN and emit anyway. This is the branch that must not
    //     throw. `/models` refreshes an ID to a model that postdates the ceilings the user has
    //     recorded, and a throw here would make the refresh command BRICK the emit it exists to
    //     keep working. Silently accepting the effort is the other trap — it is exactly the
    //     illegal-level bug the throw was added to prevent. A loud warning fails neither way:
    //     the emit completes, and the user is told which model to record levels for.
    var effort = parsed.fm.effort;
    if (effort) {
      var allowed = supportedEfforts(resolved, model);
      if (!allowed) {
        warn(
          '.claude/agents/' + name + '.md pins effort "' + effort + '", but ' + model +
          ' has no recorded reasoning levels in .claude/harness.json -> models.efforts, so the ' +
          'effort could not be validated. Emitting it as-is: an unsupported level will fail at ' +
          'dispatch time. Run /models to record ' + model + "'s levels (its catalog lists them " +
          'as supported_reasoning_levels).'
        );
      } else if (allowed.indexOf(effort) === -1) {
        throw new Error(
          '.claude/agents/' + name + '.md pins effort "' + effort + '", which ' + model +
          ' does not support (allowed: ' + allowed.join(', ') + '). ' +
          'Lower the effort or raise the tier. If ' + model + ' really does support "' + effort +
          '", the map is out of date — run /models to re-verify .claude/harness.json -> models.'
        );
      }
      lines.push('model_reasoning_effort = ' + tomlBasic(effort));
    }
  }

  lines.push('developer_instructions = ' + tomlMultiline(body), '');
  return lines.join('\n');
}

// A TOML bare key where the name allows it; a quoted key otherwise. Codex accepts
// letters, digits, spaces, hyphens and underscores in an agent name — and a space
// is not a legal bare key.
function tomlKey(name) {
  return /^[A-Za-z0-9_-]+$/.test(name) ? name : tomlBasic(name);
}

// Codex has NO directory auto-scan of .codex/agents/ — an agent file that
// config.toml does not register does not exist, and the model key inside it is
// never read. So the registration is built FROM the files we just emitted, and
// can never drift from them.
//
// `max_threads` STAYS under [agents]. It was suspected of registering a phantom
// agent named "max_threads" (if [agents] were a plain agent-name -> role map).
// It does not. [agents] is a HYBRID table: scalar tuning fields PLUS [agents.<n>]
// sub-tables. Established against codex-cli 0.144.0 with `codex doctor`, whose
// rejections are real (control: `model = 12345` -> "config could not be loaded"):
//   [agents] max_threads = 4    -> loads clean
//   [agents] bogus_scalar = 4   -> HARD error, "config could not be loaded"
// A plain name->role map would reject both integers identically, so max_threads is
// a known, typed field. Corroborated by the binary's own literal config paths
// ("agents.max_threads", "agents.max_depth", "agents.job_max_runtime_seconds") and
// by `struct OrchestratorToml with 2 elements` — too small to be their owner.
//
// `config_file` resolves relative to the config.toml's OWN directory (.codex/),
// NOT the project root: `.codex/agents/<n>.toml` resolves to
// `<project>/.codex/.codex/agents/<n>.toml` and Codex then silently drops the
// agent ("Ignoring malformed agent role definition"). Hence `agents/<n>.toml`.
function codexConfigToml(agents) {
  var lines = [
    '# ' + GENERATED_BY,
    '# Source: perfect-harness-engineering template. Re-run `npx perfect-harness-engineering update`.',
    '',
    '# Codex subagents are OFF by default.',
    '[features]',
    'multi_agent = true',
    '',
    '[agents]',
    'max_threads = 4',
    '',
  ];

  for (var i = 0; i < agents.length; i++) {
    var a = agents[i];
    lines.push(
      '[agents.' + tomlKey(a.name) + ']',
      'description = ' + tomlBasic(a.description),
      'config_file = ' + tomlBasic('agents/' + a.file + '.toml'),
      'nickname_candidates = [' + tomlBasic(a.name) + ']',
      ''
    );
  }

  return lines.join('\n');
}

const GENERATED_MARKER = [
  '# ' + GENERATED_BY,
  '#',
  '# This directory is DERIVED from .claude/skills/ on every',
  '# `perfect-harness-engineering init` / `update`. Edits here are LOST.',
  '# Edit .claude/skills/<name>/SKILL.md instead — it is the single source and',
  '# it serves both harnesses (Claude Code reads .claude/skills, Codex reads here).',
  '',
].join('\n');

function copyTree(src, dest) {
  if (!fs.existsSync(dest)) fs.mkdirSync(dest, { recursive: true });
  var entries = fs.readdirSync(src, { withFileTypes: true });
  for (var i = 0; i < entries.length; i++) {
    var entry = entries[i];
    // Parity with init.js: never follow symlinks out of the payload.
    if (entry.isSymbolicLink()) continue;
    // Never mirror backup artifacts (<file>.backup) that backupAndCopy leaves
    // in .claude/ once update.js has run — the generated Codex tree must be a
    // faithful mirror of the canonical skill content only, never PHE's own
    // backup bookkeeping from the file it was copied out of.
    if (entry.name.slice(-7) === '.backup') continue;
    var s = path.join(src, entry.name);
    var d = path.join(dest, entry.name);
    if (entry.isDirectory()) copyTree(s, d);
    else if (entry.isFile()) fs.copyFileSync(s, d);
  }
}

function dirNames(p) {
  if (!fs.existsSync(p)) return [];
  return fs.readdirSync(p, { withFileTypes: true })
    .filter(function (e) { return e.isDirectory(); })
    .map(function (e) { return e.name; });
}

// F1 (CRITICAL): every rm/mkdir/write below constructs a path UNDER one of
// .agents, .agents/skills, .codex, .codex/agents and then deletes or creates
// through it. If any of those directories -- or a single skill dir inside
// .agents/skills/ -- is a symlink, the OS resolves the symlinked component
// transparently, so `fs.rmSync('.agents/skills/<stale-name>', {recursive:true})`
// or `fs.mkdirSync(..., {recursive:true})` silently operates on whatever the
// link points to instead. Reproduced: a cloned repo shipping `.agents/skills`
// as a symlink to $HOME let the stale-skill prune loop `rm -rf` real
// directories ($HOME/Documents, $HOME/.ssh, ...); `ln -s ../.claude/skills
// .agents/skills` let it empty the CANONICAL skills through the link.
//
// The fix: lstat (never follow) every one of those paths BEFORE any
// destructive or creative call touches it, and throw hard on a symlink. This
// is a destructive operation and the user may have made the symlink
// deliberately -- silently unlinking and recreating would be worse than a
// loud refusal.
function assertNotSymlink(p) {
  var st;
  try {
    st = fs.lstatSync(p);
  } catch (e) {
    return; // does not exist yet -- nothing to guard, caller will create it
  }
  if (st.isSymbolicLink()) {
    throw new Error(
      p + ' is a symlink. perfect-harness-engineering will not write through a ' +
      'symlink into a generated tree (.agents/ or .codex/) -- doing so could ' +
      'delete or overwrite content outside the intended tree. Remove the ' +
      'symlink (or move what it points to somewhere else) and re-run.'
    );
  }
}

// A generated-tree directory must be a real directory (or absent) -- never a
// symlink, never a plain file blocking the spot.
function assertRealDir(p) {
  assertNotSymlink(p);
  if (fs.existsSync(p) && !fs.statSync(p).isDirectory()) {
    throw new Error(p + ' exists but is not a directory -- refusing to write the generated tree there.');
  }
}

// Guard every direct child of a generated directory, not just the directory
// itself -- a single skill dir being a symlink is exactly as dangerous as
// .agents/skills itself being one (rm/mkdir/write would resolve through it
// the same way). No-ops when dirPath does not exist yet.
function assertNoSymlinkChildren(dirPath) {
  if (!fs.existsSync(dirPath)) return;
  var names = fs.readdirSync(dirPath);
  for (var i = 0; i < names.length; i++) {
    assertNotSymlink(path.join(dirPath, names[i]));
  }
}

// Derive the Codex tree from the installed .claude/ payload.
// Generated files are overwritten, never backed up — they are not user content.
//
// `onWarn` receives every non-fatal problem (see agentMdToToml). Defaults to stderr; the
// warnings are also returned on `counts.warnings` so a caller can re-report them.
function emitCodexPayload(projectRoot, onWarn) {
  var claudeSkills = path.join(projectRoot, '.claude', 'skills');
  var claudeAgents = path.join(projectRoot, '.claude', 'agents');
  var agentsRoot = path.join(projectRoot, '.agents');
  var agentsSkills = path.join(projectRoot, '.agents', 'skills');
  var codexRoot = path.join(projectRoot, '.codex');
  var codexAgents = path.join(projectRoot, '.codex', 'agents');
  var counts = { skills: 0, agents: 0, warnings: [] };
  var models = readModels(projectRoot) || DEFAULT_MODELS;
  var registered = [];
  var emitWarning = onWarn || function (msg) { console.warn('WARNING: ' + msg); };
  var warn = function (msg) { counts.warnings.push(msg); emitWarning(msg); };

  // F1: guard every level of the generated tree BEFORE any rm/mkdir/write
  // touches it (see assertRealDir/assertNoSymlinkChildren above). Parent
  // checked before child, so a symlinked .agents (or .codex) is caught before
  // we ever try to resolve a path underneath it.
  assertRealDir(agentsRoot);
  assertRealDir(agentsSkills);
  assertNoSymlinkChildren(agentsSkills);
  assertRealDir(codexRoot);
  assertRealDir(codexAgents);
  assertNoSymlinkChildren(codexAgents);

  // --- RENDER EVERY AGENT BEFORE WRITING ANYTHING ---
  // agentMdToToml throws on an unknown tier or an illegal effort, and it used to do so from
  // inside the write loop — after the skills had been copied and some agent TOMLs written,
  // leaving a MIXED old/new generated tree that Codex would happily load. Rendering is pure,
  // so it all happens up front: an emit that cannot complete must not have started.
  var rendered = [];
  if (fs.existsSync(claudeAgents)) {
    var agentFiles = fs.readdirSync(claudeAgents).filter(function (f) { return f.endsWith('.md'); });
    for (var g = 0; g < agentFiles.length; g++) {
      var agentStem = agentFiles[g].replace(/\.md$/, '');
      var agentMd = fs.readFileSync(path.join(claudeAgents, agentFiles[g]), 'utf-8');
      rendered.push({ stem: agentStem, toml: agentMdToToml(agentMd, agentStem, models, warn) });

      // Register under the name the TOML actually declares (frontmatter `name:` wins over the
      // stem, exactly as agentMdToToml resolves it) but point config_file at the FILE, which is
      // always named for the stem. Conflating the two would emit a dangling config_file whenever
      // they differ, and Codex drops such an agent with only a startup warning.
      var agentFm = parseFrontmatter(agentMd).fm;
      registered.push({
        name: agentFm.name || agentStem,
        file: agentStem,
        description: agentFm.description || '',
      });
    }
  }

  // --- skills: .claude/skills/<n>/ -> .agents/skills/<n>/ ---
  fs.mkdirSync(agentsSkills, { recursive: true });
  var wanted = dirNames(claudeSkills);

  // Prune skills we previously generated but no longer ship, so a removed skill
  // does not keep firing on Codex after an update.
  var existing = dirNames(agentsSkills);
  for (var p = 0; p < existing.length; p++) {
    if (wanted.indexOf(existing[p]) === -1) {
      fs.rmSync(path.join(agentsSkills, existing[p]), { recursive: true, force: true });
    }
  }

  for (var s = 0; s < wanted.length; s++) {
    var skillDest = path.join(agentsSkills, wanted[s]);
    // Clean-then-copy: the generated skill dir must be a faithful mirror of
    // the source, not an accumulating overlay. copyTree only adds/overwrites,
    // so a file dropped from a still-shipped skill would otherwise linger
    // forever. This is safe because .agents/skills/ is wholly generated.
    fs.rmSync(skillDest, { recursive: true, force: true });
    copyTree(path.join(claudeSkills, wanted[s]), skillDest);
    counts.skills++;

    // Implicit-invocation parity. Claude Code keeps a skill user-invoke-only with
    // `disable-model-invocation: true`; Codex's equivalent is an openai.yaml
    // policy alongside SKILL.md. Without it Codex would happily auto-fire
    // /implement off a description match — a pipeline stage must never self-start.
    var skillMd = null;
    try {
      skillMd = fs.readFileSync(path.join(skillDest, 'SKILL.md'), 'utf-8');
    } catch (e) {
      skillMd = null;
    }
    var yamlPath = path.join(skillDest, 'agents', 'openai.yaml');
    var userOnly = skillMd !== null &&
      parseFrontmatter(skillMd).fm['disable-model-invocation'] === 'true';

    if (userOnly) {
      fs.mkdirSync(path.join(skillDest, 'agents'), { recursive: true });
      fs.writeFileSync(yamlPath, [
        '# ' + GENERATED_BY,
        '# Mirrors `disable-model-invocation: true` in the canonical SKILL.md.',
        'policy:',
        '  allow_implicit_invocation: false',
        '',
      ].join('\n'));
    }
    // No stale-delete branch needed here: skillDest was just removed and
    // recopied from scratch above, so a fresh dir can never contain a stale
    // agents/openai.yaml (or a stale, now-empty agents/ dir) to clean up.
  }
  fs.writeFileSync(path.join(agentsSkills, '.phe-generated'), GENERATED_MARKER);

  // --- agents: .claude/agents/<n>.md -> .codex/agents/<n>.toml ---
  // Pure writes: everything that can throw already ran above.
  fs.mkdirSync(codexAgents, { recursive: true });
  for (var a = 0; a < rendered.length; a++) {
    fs.writeFileSync(path.join(codexAgents, rendered[a].stem + '.toml'), rendered[a].toml);
    counts.agents++;
  }

  // --- .codex/config.toml ---
  // F1 residual: config.toml is a direct child of .codex, not of .codex/agents,
  // so assertNoSymlinkChildren(codexAgents) above never covers it. Without this,
  // a symlinked .codex/config.toml lets writeFileSync follow the link and
  // overwrite whatever it points at. .codex itself is already covered by
  // assertRealDir(codexRoot) above.
  var codexConfigPath = path.join(codexRoot, 'config.toml');
  assertNotSymlink(codexConfigPath);
  fs.writeFileSync(codexConfigPath, codexConfigToml(registered));

  return counts;
}

// F2: remove the whole generated Codex tree (.agents/, .codex/) -- called
// when `codex` drops out of the recorded harness targets (e.g. init(both)
// re-run as init(claude)). Everything under .agents/.codex is generated and
// holds nothing the user owns, so wholesale removal is safe -- but this is
// the SAME destructive primitive as the stale-skill prune loop, so the SAME
// F1 symlink guard applies: refuse rather than rm -rf through a symlink.
function removeCodexPayload(projectRoot) {
  var agentsRoot = path.join(projectRoot, '.agents');
  var codexRoot = path.join(projectRoot, '.codex');
  var removed = { agents: false, codex: false };

  assertRealDir(agentsRoot);
  assertRealDir(codexRoot);

  if (fs.existsSync(agentsRoot)) {
    fs.rmSync(agentsRoot, { recursive: true, force: true });
    removed.agents = true;
  }
  if (fs.existsSync(codexRoot)) {
    fs.rmSync(codexRoot, { recursive: true, force: true });
    removed.codex = true;
  }
  return removed;
}

// F2: run at the same point init.js/update.js do the conditional emit.
// Removes the generated tree(s) for any harness target no longer present.
// codex dropped -> .agents/+.codex/ go. claude dropped -> the CLAUDE.md shim
// goes too (it is inert -- an @AGENTS.md import, never user content of its
// own -- so removing it is as safe as removing .agents/.codex). Only one of
// the two can happen per run: parseHarnessAnswer only ever offers
// claude/codex/both, never "neither", so targets can shrink by at most one
// harness per init/update. Returns a single never-silent notice line, or
// null when there was nothing to remove.
function cleanupDroppedTargets(projectRoot, targets) {
  var removeClaudeMdShim = require('./claude-md-copy').removeClaudeMdShim;
  var removedParts = [];
  var droppedNames = [];

  if (targets.indexOf('codex') === -1) {
    var removedCodex = removeCodexPayload(projectRoot);
    if (removedCodex.agents) removedParts.push('.agents/');
    if (removedCodex.codex) removedParts.push('.codex/');
    if (removedCodex.agents || removedCodex.codex) droppedNames.push('codex');
  }
  if (targets.indexOf('claude') === -1) {
    if (removeClaudeMdShim(projectRoot)) {
      removedParts.push('CLAUDE.md');
      droppedNames.push('claude');
    }
  }

  if (removedParts.length === 0) return null;
  return 'Removed the Codex payload (' + removedParts.join(', ') + ') -- ' +
    droppedNames.join(' + ') + ' is no longer a target.';
}

module.exports = {
  GENERATED_BY: GENERATED_BY,
  agentMdToToml: agentMdToToml,
  codexConfigToml: codexConfigToml,
  parseFrontmatter: parseFrontmatter,
  emitCodexPayload: emitCodexPayload,
  removeCodexPayload: removeCodexPayload,
  cleanupDroppedTargets: cleanupDroppedTargets,
};
